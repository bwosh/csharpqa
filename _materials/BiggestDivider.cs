using System;

namespace Fibbonaci
{
    class Program
    {
        static void Main(string[] args)
        {
            int a = 60;
            int b = 36;
            int divider = FindBiggestDivider(a, b);
            Console.WriteLine(divider);
            Wait();
        }

        private static void Wait()
        {
            Console.ReadLine();
        }

        private static int FindBiggestDivider(int a, int b)
        {
            var min = GetMinimumValue(a, b);

            for (int i = min; i >= 1; i--)
            {
                if (IsDivadibleBy(a, i) && IsDivadibleBy(b, i))
                {
                    return i;
                }
            }

            return 1;
        }

        static bool IsDivadibleBy(int number, int divider)
        {
            return number % divider == 0;
        }

        static int GetMinimumValue(int x, int y)
        {
            return x < y ? x : y;
        }
    }
}


